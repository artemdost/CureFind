export function getClinicsByRegion(regionId: string): Clinic[] {
  return clinics.filter(c => c.regionId === regionId);
}

export function getClinicById(id: string): Clinic | undefined {
  return clinics.find(c => c.id === id);
}

export function searchClinics(query: string, regionId?: string, specialty?: string): Clinic[] {
  let results = [...clinics];

  if (regionId) {
    results = results.filter(c => c.regionId === regionId);
  }

  if (specialty) {
    results = results.filter(c => c.specialties.includes(specialty));
  }

  if (query) {
    const q = query.toLowerCase();
    results = results.filter(c =>
      c.name.toLowerCase().includes(q) ||
      c.description.toLowerCase().includes(q) ||
      c.specialties.some(s => s.toLowerCase().includes(q)) ||
      c.doctors.some(d => d.name.toLowerCase().includes(q) || d.specialty.toLowerCase().includes(q)) ||
      c.services.some(s => s.name.toLowerCase().includes(q))
    );
  }

  return results;
}
